    private void btnSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacarActionPerformed
        String sNumero = txtNConta.getText();
        String sSenha = txtSenha.getText();
        String sSaque = txtSaque.getText();
        
        int numero = Integer.parseInt(sNumero);
        int senha = Integer.parseInt(sSenha);
        double saque = Double.parseDouble(sSaque);
        
        //Consultar a conta
        ContaBancaria c = bb.consultar(numero,senha);
        
        if (c==null) {
            JOptionPane.showMessageDialog(null,"ERRO - Conta ou Senha inv√°lida ");
            txtNConta.setText("");
            txtSenha.setText("");
            txtSaque.setText("");
        } else {
            if (saque <= c.getSaldo()) {
                c.sacar(saque);
                JOptionPane.showMessageDialog(null, "Saque na conta realizado com sucesso! ");
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, " Saldo insuficiente ");
                txtNConta.setText("");
                txtSenha.setText("");
                txtSaque.setText("");
            }
        }
    }//GEN-LAST:event_btnSacarActionPerformed

