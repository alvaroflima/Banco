    private void btnDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDActionPerformed
        String sNumero = txtNConta.getText();
        String sValor = txtValor.getText();
        
        int numero = Integer.parseInt(sNumero);
        double valor = Double.parseDouble(sValor);
        
        //Consultar a conta
        ContaBancaria c = bb.deposito(numero,valor);
        
        if (c==null) {
            JOptionPane.showMessageDialog(null,"ERRO - Conta n√£o encontrada");
            txtNConta.setText("");
            txtValor.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Deposito realizado com sucesso!");
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnDActionPerformed

