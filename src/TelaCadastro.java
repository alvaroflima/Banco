    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        String nome = txtNome.getText().toUpperCase();
        String sNumero = txtNumero.getText();
        String sSenha = txtSenha.getText();
        
        
        int numero = Integer.parseInt(sNumero);
        int senha = Integer.parseInt(sSenha);
        
        //criar a conta
        ContaBancaria conta = new ContaBancaria(nome, numero, senha);
        
        //adiciona no banco
        bb.adicionar(conta);
        
        JOptionPane.showMessageDialog(null, "Conta bancaria cadastrada com sucesso!");
        
        int input = JOptionPane.showConfirmDialog(null, "Deseja continuar cadastrando?");
        // 0=yes, 1=no, 2=cancel
        switch(input) {
        case 0:
          txtNumero.setText("");
          txtSenha.setText("");
          txtNome.setText("");
        break;
        case 1:
          this.setVisible(false);
        break;
        case 2:
          this.setVisible(true);
        break;
        }
        
    }//GEN-LAST:event_btnCadastroActionPerformed

